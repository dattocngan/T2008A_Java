    void showData(){
        tableModel.setRowCount(0);
        for (Book book : dataList) {
            tableModel.addRow(new Object[]{tableModel.getRowCount()+1,book.title,book.price,
                book.manu,book.author,book.publishDate});
            BookModify.insert(book);
        }
    }
    void resetData(){
        txtAuthor.setText("");
        txtManu.setText("");
        txtPrice.setText("");
        txtPublishDate.setText("");
        txtTitle.setText("");
    }
