    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Book book = new Book();
        book.author = txtAuthor.getText();
        book.title = txtTitle.getText();
        book.price = Float.parseFloat(txtPrice.getText());
        book.manu = txtManu.getText();
        book.author = txtAuthor.getText();
        book.publishDate = txtPublishDate.getText();
        dataList.add(book);
        resetData();
        showData();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(index >= 0){
            int option = JOptionPane.showConfirmDialog(rootPane, "Ban co chac chan muon xoa ko?");
            if(option != 0) return;
            dataList.remove(index);
            BookModify.delete();
            showData();
            resetData();
            index = -1;
        }else{
            JOptionPane.showMessageDialog(rootPane, "Khong co phan tu nao duoc xoa");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

